package ru.lawNotes.calc.parts.models;

public class PartModelResult {
    private String initialObjPrice;
    private String initialObjSquare;
    private String initialObjPartNumerator;
    private String initialObjPartDenominator;
    private String finalObjPrice;
    private String finalObjSquare;

    private String partMin;

    private String partMinPrice;
    private String partMinSquare;
    private String partPrice;
    private String partSquare;




    public PartModelResult() {
    }

    public String getInitialObjPrice() {
        return initialObjPrice;
    }

    public void setInitialObjPrice(String initialObjPrice) {
        this.initialObjPrice = initialObjPrice;
    }

    public String getInitialObjSquare() {
        return initialObjSquare;
    }

    public void setInitialObjSquare(String initialObjSquare) {
        this.initialObjSquare = initialObjSquare;
    }

    public String getInitialObjPartNumerator() {
        return initialObjPartNumerator;
    }

    public void setInitialObjPartNumerator(String initialObjPartNumerator) {
        this.initialObjPartNumerator = initialObjPartNumerator;
    }

    public String getInitialObjPartDenominator() {
        return initialObjPartDenominator;
    }

    public void setInitialObjPartDenominator(String initialObjPartDenominator) {
        this.initialObjPartDenominator = initialObjPartDenominator;
    }

    public String getFinalObjPrice() {
        return finalObjPrice;
    }

    public void setFinalObjPrice(String finalObjPrice) {
        this.finalObjPrice = finalObjPrice;
    }

    public String getFinalObjSquare() {
        return finalObjSquare;
    }

    public void setFinalObjSquare(String finalObjSquare) {
        this.finalObjSquare = finalObjSquare;
    }

    public String getPartPrice() {
        return partPrice;
    }

    public void setPartPrice(String partPrice) {
        this.partPrice = partPrice;
    }

    public String getPartSquare() {
        return partSquare;
    }

    public void setPartSquare(String partSquare) {
        this.partSquare = partSquare;
    }

    public String getPartMin() {
        return partMin;
    }

    public void setPartMin(String partMin) {
        this.partMin = partMin;
    }

    public String getPartMinPrice() {
        return partMinPrice;
    }

    public void setPartMinPrice(String partMinPrice) {
        this.partMinPrice = partMinPrice;
    }

    public String getPartMinSquare() {
        return partMinSquare;
    }

    public void setPartMinSquare(String partMinSquare) {
        this.partMinSquare = partMinSquare;
    }
}
